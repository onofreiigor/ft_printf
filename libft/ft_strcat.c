#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int index;
	int b;

	index = 0;
	b = 0;
	while (dest[index])
		index++;
	while (src[b])
		dest[index++] = src[b++];
	dest[index] = '\0';
	return (dest);
}
