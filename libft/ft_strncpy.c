#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, int n)
{
	int index;

	index = 0;
	while (index < n && src[index])
	{
		dest[index] = src[index];
		index++;
	}
	while (index < n)
		dest[index++] = '\0';
	return (dest);
}
