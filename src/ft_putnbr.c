#include "libft.h"

int	ft_putnbr(int n)
{
	int res;

	res = 0;
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		n = -n;
		res += ft_putchar('-');
	}
	if (n > 9)
	{
		res += ft_putnbr(n / 10);
	}
	res += ft_putchar((n % 10) + '0');
	n /= 10;
	return (res);
}
